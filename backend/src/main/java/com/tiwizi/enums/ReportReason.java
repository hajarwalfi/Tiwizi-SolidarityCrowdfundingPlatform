package com.tiwizi.enums;

/**
 * Reasons for reporting a campaign
 */
public enum ReportReason {
    SPAM,              // Spam or irrelevant content
    FRAUD,             // Fraudulent or fake campaign
    INAPPROPRIATE,     // Inappropriate or offensive content
    DUPLICATE,         // Duplicate of another campaign
    FALSE_INFO,        // False or misleading information
    OTHER              // Other reason (see description)
}
